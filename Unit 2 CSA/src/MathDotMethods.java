
public class MathDotMethods {

	public static void main(String[] args) {

		int num1 = -64;
		int base = 3;
		int exp = 2;
		int num2 = 6;
		int num3 = 8;
	
		System.out.println(Math.PI);
		System.out.println(Math.E);
		System.out.println(Math.exp(1));
		System.out.println(Math.abs(num1));
		System.out.println(Math.pow(base, exp));
		System.out.println(Math.max(num2, num3));
		System.out.println(Math.min(num2, num3));
		System.out.println(Math.toDegrees((Math.PI*10)/6));
		System.out.println((Math.random()*5));
		System.out.println(Math.cbrt(num3));
	}

}
